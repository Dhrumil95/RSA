    private void createActionPerformed(java.awt.event.ActionEvent evt) throws SAXException, TransformerException, ParserConfigurationException, IOException {


        String encrypted_filename = JOptionPane.showInputDialog("Enter encrypted block file name: ");

        String blocked_filename = JOptionPane.showInputDialog("Enter block file name: ");
        String key_file = JOptionPane.showInputDialog("Enter private key file name: ");
        RSA.Key key = RSA.getKey(key_file);
        BufferedReader reader = new BufferedReader(new FileReader(encrypted_filename));
        PrintWriter writer = new PrintWriter(new File(blocked_filename));
        while (true) {
            String line = reader.readLine();
            if (line == null) break;
            writer.println(RSA.decrypt(new LargeInteger(line), key.n, key.e));

        }
        writer.close();
        JOptionPane.showMessageDialog(null, "Successfully decrypted block file!");


    }
